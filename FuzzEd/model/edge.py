from django.db import models

from node import Node

class Edge(models.Model):
    """
    Edge class

    Models a generic edge that connects two nodes with each other.

    Attributes:
    client_id -- the id of this edge as it was generated by the client (integer)
    source    -- starting point of the edge (Node, required)
    target    -- endpoint of the edge (Node, required)
    deleted   -- flag indicating whether the edge was deleted (boolean, required, default: False)
    """
    class Meta:
        app_label = 'FuzzEd'

    client_id = models.BigIntegerField()
    source    = models.ForeignKey(Node, null=False, related_name='outgoing')
    target    = models.ForeignKey(Node, null=False, related_name='incoming')
    deleted   = models.BooleanField(default=False)

    def __unicode__(self):
        return  '%s -> %s' % (str(self.source), str(self.target))

    def to_json(self):
        """
        Serializes the values of the edge into a python dictionary that is JSON conform.

        Returns:
        dictionary
        """
        return {
            'id':     self.client_id, 
            'source': self.source.client_id, 
            'target': self.target.client_id
        }